			<p class="lead">DŽIMS – astoņgadīgs jauneklis. Kaut arī pēc pases par jaunekli īsti vairs nav saucams, Džima dzīvesprieks, kustīgums un atsperīgums liek domāt par jaunības eliksīra esamību vai vismaz par lieliskiem sportista gēniem.
Divus ar pusi gadus nepelnīti nepamanīts patversmes voljērā, skaistais, sportiskais Džims ļoti noilgojies pēc mājām, draudzības un cilvēku sabiedrības.

Kā jau katram no mums, Džimim ir sava gaišā un tumšā puse jeb lietas, ar ko lepojamies un lietas, ko labāk gribētos paslaucīt zem paklājiņa. Sāksim ar tām otrajām. Uzreiz jāteic, ka Džims ir augstlēcējs – “paņems” jebkuru žogu, sētu, mūri. Nevis tāpēc, lai izlauztos un aizmuktu, bet tāpēc, ka sportisks azarts pārbaudīt sevi un augstumu. Un ja vēl žoga otrā pusē ir kāds sugas brālis, tad azarts pieaug trīskārt. Jo otra Džima “slidenā” lieta ir nīgrums pret citiem suņiem un vēlme parādīt savu pārākumu. Kas zina, kādēļ dzīve Džimim to iemācījusi – iespējams, pats kādreiz bijis upura lomā.

Taču mēs gribam, lai šī godīgi atklātā Džima tumšā puse “nenoraktu” viņa iespēju atrast mājas. Vai ar to iespējams sadzīvot? Jā, protams, ja ir atbilstoši apstākļi!
1) Dzīvoklī vai iekštelpās nav kārdinājuma “paņemt”augstumu, tāpat arī voljērā ar jumtu (sietu). Iekštelpās Džims māk kārtīgi uzvesties un dzīvos labprāt, ja būs ikdienas garās pastaigas un sportiskas aktivitātes. Pie pavadas iet ar prieku, uzvedas rātni un pieskaņojas cilvēkam. Taču uzskata par savu pienākumu pieskatīt un sargāt savējos – gan teritorijā, gan pastaigas laikā – tā, viņaprāt, izpaužas uzticība.
2) Būtu vēlams, lai Džimim visu laiku acu priekšā nebūtu citi sugasbrāļi – tas kaitina un liek vicināt dūres, parādīt savu spēku. Mierīgos apstākļos Džims arī pats būs mierīgs un mūsu kinologs Ingus jaunajam saimniekam un Džimim labprāt pasniegs kopīgas pieklājīgas uzvedības stundas. Starp citu, pret draudzīgām suņu meitenēm Džims tīri labi māk izturēties kā džentlmenis, tomēr labāk, ja viņš būs vienīgais suns ģimenē.
Visādi citādi – Džims ir fantastisks draugs un sabiedrotais spēlēs un ikdienas gaitās. Cilvēkus, kurus iepazīst vairāk par vienu reizi, mīl un seko uz soļa. Azartiskums ir rakstura īpašība, tāpēc tas izpaužas ne tikai augstlēkšanā, bet arī bumbiņspēlē, pēdu meklēšanā, rotaļās, skriešanas sacensībās, samīļošanā un visās citās jautrībās, ko kopīgi var darīt suns un cilvēks. Ja vien cilvēkam vajadzīgs suns. Tik foršs suns kā Džims.

Sajutis sev pievērstu uzmanību, Džims atplaukst smaidā un liek smaidīt arī mums.
Viņš gaida, kad varēs uzsmaidīt savam saimniekam. Un Tu?
